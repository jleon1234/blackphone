    public function index()
    {
        $fecha = new \DateTime();
        $fechaActual = $fecha->format('Y-m-d');
        $ventas = Ventas::whereDate("created_at", $fechaActual)->get();
        return view('ventas.index', compact('ventas', 'fechaActual'));
